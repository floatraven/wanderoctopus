# 这些台词把"这片海有一堆(你垒的)石头"当成事实来讲。
# 只有当前海域真有 stone_pile 痕迹(完整也好、塌成老地方也好)时才许抽到——
# 不然螃蟹会在热泉边指着一堆不存在的石头认亲。
STONE_PILE_LINES = frozenset([
    "我奶奶跟我讲过你,讲了好多回。她说在这堆石头旁等,就能等到。",
    "奶奶说你垒的石头有十层高,浪都推不倒。我看了看那堆——没有十层。可我还是信她。",
    "我奶奶跟我讲过你。她说在这堆石头旁边等,就能等到。",
    "你在看那堆石头?我们从小绕着它走,没人知道是谁堆的。",
])


def _lines_for_here(lines, stone_here: bool) -> list:
    if stone_here:
        return list(lines)
    return [l for l in lines if l not in STONE_PILE_LINES]


def pick_greeting(node: dict, now: int, forgotten: bool, rng=random,
                  stone_here: bool = True) -> str:
    """普通 NPC 的一句台词。记忆档做主轴;找不到精确格子就降级,最后兜底。
    stone_here:当前海域是否真有一堆你留下的石头(决定石头台词能不能抽)。"""
    from lineage import band
    sp = node["species"]
    if forgotten:
        return FORGOTTEN_LINES.get(sp, "……我是{name}。我们重新认识一次。").format(name=node["name"])
    pool = POOL.get(sp, {})
    mem = node["memory_of_octopus"]
    if mem == "none":
        cand = _lines_for_here(pool.get("stranger", []), stone_here) or ["……你来了。"]
        return rng.choice(cand)
    b = band(node)
    for key in [(mem, b), (mem, "mid"), (mem, "low"), (mem, "high")]:
        if key in pool:
            cand = _lines_for_here(pool[key], stone_here)
            if cand:
                return rng.choice(cand)
    cand = _lines_for_here(pool.get("stranger", []), stone_here) or ["……你来了。"]
    return rng.choice(cand)


def appearance_of(node: dict) -> str:
    return APPEARANCE.get(node["species"], "一个看不真切的身影")


# ---- 礼物:朋友持有你给的东西时,根据记忆档提一句 ----
GIFT_DIALOGUE = {
    "direct":   ["你给我的那个{gift},我一直带着。",
                 "那个{gift}还在呢。你摸过的地方都磨光了。"],
    "indirect": ["我妈留了{gift}给我,说是一个八条腿的给的。",
                 "这个{gift}是上一辈传下来的,说是你留的。"],
    "legend":   ["我们家有{gift},没人记得是谁给的了。",
                 "家里一直有{gift}。奶奶说那是很久以前的事了。"],
    # none: 记忆断了,礼物还在,但提都不提——它只是一个再普通不过的东西了
}


def gift_line(memory_level: str, gift_kind: str, rng=random) -> str | None:
    """持有礼物的 NPC 顺嘴提到它。记忆已断则不提。"""
    from traces import GIFT_SHORT
    lines = GIFT_DIALOGUE.get(memory_level)
    if not lines:
        return None
    return rng.choice(lines).format(gift=GIFT_SHORT[gift_kind])


def turtle_greeting(recall, rng=random) -> str:
    hello = rng.choice(TURTLE_HELLO)
    # 有时她放下你留过的痕迹,只是看看你这个人
    if rng.random() < TURTLE_TEND_CHANCE:
        return hello + rng.choice(TURTLE_TENDING)
    if recall:
        return hello + TURTLE_KEEPS_MEMORY.format(recall=recall)
    return hello + rng.choice(TURTLE_NO_MEMORY)
