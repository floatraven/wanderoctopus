def roll_encounter(region_id: str, rng: random.Random, is_travel: bool = False) -> str | None:
    if is_travel:
        pool = TRAVEL_ENCOUNTERS
    else:
        pool = REGION_ENCOUNTERS.get(region_id, [])
    if not pool:
        return None
    return rng.choice(pool)
